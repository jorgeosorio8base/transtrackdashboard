          /**
           * Hook to fetch list of Driver from the server.
           * 
           * @returns {UseQueryResult<{items: IDriverEntity[], count: number} | null>}
           * 
           * @example
           * const {data, isError, isLoading, isFetching, refetch} = useDriverList({first: 10});
           * 
           * @param {QueryDriversListArgs} variables - The query variables.
          */

          export function useDriverList(
              variables?: QueryDriversListArgs,
        ) {
              const {data: session} = useSession();
          return useQuery<{
              items: IDriverEntity[];
              count: number;
          } | null>(
          {
              queryKey: [
                'DRIVER_LIST_QUERY', variables
              ],
              queryFn: async () => UseCases.Driver.getDriverList({
                variables: variables || {},
                token: session?.token?.idToken,
              }),
              enabled: !!session?.token?.idToken,
          });
        }
    